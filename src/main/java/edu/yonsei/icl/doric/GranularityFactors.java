package edu.yonsei.icl.doric;

public class GranularityFactors {
	double graFacLongitude;
	double graFacLatitude;
	
	public GranularityFactors() {
		this.graFacLongitude = 0;
		this.graFacLatitude = 0;
	}

	public GranularityFactors(double graFacLongitude,
			double graFacLatitude) {
		this.graFacLongitude = graFacLongitude;
		this.graFacLatitude = graFacLatitude;
	}
	
}
